public class chapter12 {
  public static void main(String[] args) {
    int month = 12;
    int date = 31;
    System.out.println(month + "月" + date + "日"); //文字列は""で囲う
    System.out.println(7 / 2);
    System.out.println(7.0 / 2.0);
    System.out.println(7 / 2.0); //小数点表記は片方でも良い
    System.out.println((double)13 / 4); //()内に変換したいデータ型を書く
  }
}