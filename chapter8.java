public class chapter8{
  public static void main(String[] args){
    int number = 11;
    String text = "Ruby";
    System.out.println(number);
    System.out.println(text);

    number = 9;  //定義を上書き データ型を書かない
    System.out.println(number);
    text = "Java";  //定義を上書き データ型を書かない
    System.out.println(text);
  }
}